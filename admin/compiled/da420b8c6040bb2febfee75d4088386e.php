									<div class="row"><h2><br />
<strong>Стеклянные офисные перегородки &laquo;ИРЛАЙН ТРЕЙД&raquo;</strong>&nbsp;</h2>

<p>Стёкла примыкают друг к другу полированной кромкой и склеиваются двойным прозрачным скотчем. Поэтому стекло может быть только закалённым 8 &ndash; 10 мм. Может быть двойным или одинарным, в зависимости от требований по звукоизоляции.&nbsp;</p>

<div><strong>Отличительные особенности:&nbsp;</strong><br />
​― Можно добиться очень хорошей звукоизоляции, если использовать стёкла разной толщины, эффект достигается за счёт разных резонансов стекла с разной толщиной, допустим один ряд 8 мм., второй 10 мм.</div>

