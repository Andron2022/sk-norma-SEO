									<div class="row"><h1 style="text-align: center;"><strong>Стеклянные офисные перегородки</strong></h1>

<p>Очень часто клиент выбирает стеклянные перегородки по визуальному признаку, при этом не учитывая другие характеристики, такие как: звукоизоляция, необходимость провести электрику, наличие закладной конструкции, необходимая прочность, безопасность, устойчивость, не забываем и цену.<br />
<br />
Допустим для детских садов, школ, учебных заведений параметр безопасности в приоритете, относительно визуального. Для переговорной, приоритетный параметр, обычно, является звукоизоляция. Для витрин бутиков важна прочность и отсутствие стоек между стёкол, чтобы не закрывать обзор товара. Очень часто есть необходимость крепить тяжёлые устройства на стеклянные перегородки, такие как раздвижные двери, роллеты, элементы рекламы мониторы и т.п.<br />
<br />
Для того, чтобы найти консенсус между визуальным и техническим параметром, предлагаем 4 системы стеклянных перегородок, которые отличаются конструктивом. Мы поможем вам с выбором, предложим оптимальный вариант конструкции, подскажем где можно сэкономить.</p></div>
																						
			
						
									<div class="row"><h2><br />
<strong>Стеклянная офисная перегородка &laquo;ИРЛАЙН УНИВЕРСАЛ&raquo;&nbsp;</strong></h2>

<p>Это самый оптимальный вариант с соотношением цена &ndash; качество. Эта система самая крепкая, лучшая звукоизоляция, на неё можно весить максимальные возможные нагрузки, лёгкая и не дорогая замена стекла, можно монтировать без примыкания к потолку.&nbsp;</p>

<div><strong>Отличительные особенности:&nbsp;</strong><br />
​― Можно добиться очень хорошей звукоизоляции, если использовать стёкла разной толщины, эффект достигается за счёт разных резонансов стекла с разной толщиной, допустим один ряд 5&nbsp;мм., второй 8 мм.<br />
― Можно использовать сырое или закалённое стекло. Толщиной 5 - 10 мм.</div>

<div>― Стеклянные офисные перегородки с двойным или одинарным остеклением. Жалюзи, плёнка, мателюкс (матовое стекло).&nbsp;</div>

<div>― Каркас &ndash; оцинкованный стальной профиль. Стекло прижимается декоративным (прижимным) профилем к каркасу.&nbsp;</div>

<div>― Ширина прижимного профиля 32 мм. Шаг вертикальных профилей, формирующих модуль перегородки, рекомендуем не более 1200 мм.&nbsp;</div>

<div>― Отличный уровень звукоизоляции за счет склеивающего эффекта прозрачного силиконового уплотнителя со стеклом и плотной стяжки всех элементов перегородки.&nbsp;</div>

<div>― Перегородка устойчива даже в случае отсутствия крепления к потолку.&nbsp;</div>

<div>― Исключено дребезжание стекла в установленной перегородке (не клипсовое крепление профиля!!).</div></div>
																						
			
						
